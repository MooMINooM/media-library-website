-- ================================================
-- สร้าง Table: calendar_events
-- สำหรับระบบปฏิทินกิจกรรมโรงเรียน
-- วิธีใช้: ไปที่ Supabase Dashboard > SQL Editor > วางโค้ดนี้แล้วกด Run
-- ================================================

CREATE TABLE IF NOT EXISTS public.calendar_events (
    id          bigserial PRIMARY KEY,
    title       text          NOT NULL,
    type        text          NOT NULL DEFAULT 'other'
                CHECK (type IN ('academic','activity','holiday','exam','other')),
    start_date  date          NOT NULL,
    end_date    date,
    description text,
    location    text,
    created_at  timestamptz   DEFAULT now()
);

-- เปิด Row Level Security (RLS)
ALTER TABLE public.calendar_events ENABLE ROW LEVEL SECURITY;

-- ✅ Policy: อ่านได้ทุกคน (สำหรับแสดงหน้าเว็บ)
CREATE POLICY "public_read_calendar"
    ON public.calendar_events
    FOR SELECT
    USING (true);

-- ✅ Policy: เขียน/แก้ไข/ลบได้เฉพาะ Admin ที่ล็อกอินแล้ว
CREATE POLICY "admin_write_calendar"
    ON public.calendar_events
    FOR ALL
    USING (auth.role() = 'authenticated')
    WITH CHECK (auth.role() = 'authenticated');

